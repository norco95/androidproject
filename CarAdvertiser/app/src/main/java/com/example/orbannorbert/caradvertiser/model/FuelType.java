package com.example.orbannorbert.caradvertiser.model;

public class FuelType {

    private String id;
    private String name;

    public FuelType(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
